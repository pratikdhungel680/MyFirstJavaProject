
public class DataTypeDemo {
    public static void main(String[] args) {
        // 1. int: Used for whole numbers (integers)
        int age = 25;

        // 2. double: Used for decimal numbers with high precision
        double price = 19.99;

        // 3. boolean: Used for logical values (true or false)
        boolean isJavaFun = true;

        // 4. char: Used for a single character (must use single quotes)
        char grade = 'A';

        // 5. String: Used for a sequence of characters (must use double quotes)
        String message = "Hello, World!";

        // Printing the values to the console
        System.out.println("Integer value: " + age);
        System.out.println("Double value: " + price);
        System.out.println("Boolean value: " + isJavaFun);
        System.out.println("Character value: " + grade);
        System.out.println("String value: " + message);
    }
}

